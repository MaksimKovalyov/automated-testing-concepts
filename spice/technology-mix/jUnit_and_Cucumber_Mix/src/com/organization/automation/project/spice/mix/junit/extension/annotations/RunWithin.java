package com.organization.automation.project.spice.mix.junit.extension.annotations;

import java.lang.annotation.Documented;
import java.lang.annotation.ElementType;
import java.lang.annotation.Inherited;
import java.lang.annotation.Retention;
import java.lang.annotation.RetentionPolicy;
import java.lang.annotation.Target;

import org.junit.runner.Runner;
import org.junit.runners.JUnit4;

import cucumber.api.junit.Cucumber;

@Retention(RetentionPolicy.RUNTIME)
@Documented
@Target(ElementType.TYPE)
@Inherited
public @interface RunWithin {
    Class<? extends Runner> eclipse()       default Cucumber.class;
    Class<? extends Runner> defaultRunner() default JUnit4.class;
}
